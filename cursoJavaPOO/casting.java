package cursoJavaPOO;

public class casting {

	public static void main(String[] args) {
		
		///casting de valores declarados (Int to double)
		
		int x, y;
		double a;
		
		x = 5;
		y = 2 * x;
		a = 2 * x;
		
		System.out.println(x);
		System.out.println(y);
		System.out.println(a);
		
		
	}

}
